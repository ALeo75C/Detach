def create_brand(hash)
  Brand.create!(name: hash[:name], logo: hash[:logo], description: hash[:description])
end
def create_products(hash, brand)
  hash[:product].each do |product|
    pr = Product.create!(brand_id: brand.id, name: product[:name], description: product[:about], image: product[:image])

    create_structure(product[:structure], pr)
    comunicate_product_and_group(product)
  end
end
def create_component(hash)
  Component.create!(name: hash[:name], allergen: hash[:allergen], description: hash[:description])
end
def create_structure(components, product)
  components.each do |component|
    is_it_first_object(Component, component)
  end
end


def create_properties(hash, object)
  hash[:promist_effests].each do |effect|
    ProductPromise.create!(active_effect_id: (ActiveEffect.find_by name: effect).id, product_id: (Product.find_by name: hash[:name]).id)
  end
end
def comunicate_product_and_component(component, product)
  Structure.create!(component_id: component.id, product_id: product.id)
end
def comunicate_effects_and_component(component, effect)
  Property.create!(active_effect_id: effect.id, component_id: component.id)
end
def comunicate_effects_and_skin(skin, object)
  skin[:effects].each do |necessity|
    necessity[:list].each do |effect|
      i = SkinTypeFactor.create!(skin_type_id: object.id, active_effect_id: (ActiveEffect.find_by name: effect).id, necessity: necessity[:status])

      puts "Связь между кожей #{(SkinType.find(i.skin_type_id)).name} и эффектом #{(ActiveEffect.find(i.active_effect_id)).name} создана статус #{i.necessity}"
    end
  end
end
def comunicate_product_and_group(hash)
  ProductType.create!(product_group_id: (ProductGroup.find_by name: hash[:view].first).id, product_id: (Product.find_by name: (hash[:name])).id, name: hash[:view].last)
end


def create_brands(hash)
  hash.each do |brand|

    b = create_brand(brand)
    create_products(brand, b)

    brand[:product].each do |product|
      pr = Product.find_by name: product[:name]
      create_properties(product, pr)
      product[:structure].each do |component|
        comp = Component.find_by name: component[:name]
        comunicate_product_and_component(comp, pr)
        component[:effects].each do |effect|
          ef = ActiveEffect.find_by name: effect
          comunicate_effects_and_component(comp, ef)
        end
      end
    end
    puts b.name
  end
end

