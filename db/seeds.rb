def start_seeds
  Rake::Task['db:drop'].invoke
  Rake::Task['db:create'].invoke
  Rake::Task['db:migrate'].invoke

  create_effects(@effects)
  create_skin_types(@skins)
  create_users(@users)
  create_catalog(@catalog)
  create_brands(@brands)
  create_articles(@articles)
end


@brand_image_path = "public/uploads/brands/"
@product_image_path = "public/uploads/product/"
@avatar_path = "public/uploads/avatars/"

def brand_logo(file_name)
  File.open(File.join(Rails.root, @brand_image_path + file_name))
end
def product_image(file_name)
  File.open(File.join(Rails.root, @product_image_path + file_name))
end
def avatar(file_name)
  File.open(File.join(Rails.root, @avatar_path + file_name))
end


def create_effects(effects)
  effects.each do |effect|
    create_effect(effect)
  end
end
def create_effect(name)
  ActiveEffect.create!(name: name)
end
def create_skin_types(skins)
  skins.each do |skin|
    object = SkinType.create!(name: skin[:type], description: skin[:about])
    comunicate_effects_and_skin(skin, object)
  end
end
def create_catalog(catalog)
  catalog.each do |group_name|
    ProductGroup.create!(name: group_name)
  end
end

def create_users(users)
  users.each do |user|
    if user[:user_name] == "ADMIN"
      create_user(user, true)
    else
      create_user(user, false)
      create_profile(user)
    end
  end
end
def create_user(hash, admin)
  User.create!(email: hash[:email], password: hash[:password], encrypted_password: hash[:password], admin: admin)
end
def create_profile(model)
  pr = Profile.last
  pr.name = model[:user_name]
  pr.age = model[:age]
  skin = SkinType.find_by name: model[:skin]
  pr.skin_type_id = skin.id
  pr.avatar = avatar(model[:avatar])
  pr.save
end

def create_articles(articles)
  articles.each do |article|
    Article.create!(header: article[:header], description: article[:description], content: article[:content])
  end
end

